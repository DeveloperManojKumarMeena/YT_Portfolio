import { useEffect, useRef } from "react"




export default function ParticalsBackground(){

  const CanvasRef = useRef(null)

  useEffect(()=>{
    const canvas = CanvasRef.current;
    const ctx = canvas.getContext("2d");

    let particals =[];
    const particaleCount = 50;
    const colors = ["rgba(255,255,255,0.9)"]
    class Particale{
      constructor(){
        this.x = Math.random()*canvas.width;
        this.y = Math.random()*canvas.height;
        this.radius = Math.random()*2+1;
        this.color = colors[Math.floor(Math.random()*colors.length)]
        this.speedX = (Math.random()-0.5)*0.5;
        this.speedY = (Math.random()-0.5)*0.5;
      }
      draw(){
        ctx.beginPath();
        ctx.arc(this.x, this.y,this.radius,0,Math.PI*2)
        ctx.shadowBlur=10;
        ctx.shadowColor = this.color;
        ctx.fillStyle =this.color;
        ctx.fill()
      }
      update(){
        this.x += this.speedX;
        this.y += this.speedY;

        if(this.x<0) this.x = canvas.width;
        if(this.x>canvas.width) this.x =0;
        if(this.y<0) this.y = canvas.height;
        if(this.y>canvas.height) this.x =0;

        this.draw();

      }
    }

    function CreatePartical(){
     let particale = []
      for(let i=0; i<particaleCount;i++){
        particals.push(new Particale())
      }
    }

    function handleResize(){
      canvas.width = window.innerWidth;
      canvas.height = window.innerHeight;
      CreatePartical();
    }
    handleResize();

    window.addEventListener("resize",handleResize);
    let animationID;
    function animate(){
      ctx.clearRect(0,0,canvas.width,canvas.height);
      particals.forEach((p)=>p.update());
      animationID=requestAnimationFrame(animate);
    }
    animate();

    return ()=>{
      cancelAnimationFrame(animationID);
      window.removeEventListener("resize",handleResize)
    }


  },[])

  return(
  <canvas ref={CanvasRef} className="absolute top-0 left-0 w-full h-full pointer-events-none z-0">

  </canvas>)
}